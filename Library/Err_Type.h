#ifndef  ERR_TYPE_H
#define  ERR_TYPE_H


/**
 * @brief Null pointer definition.
 */
#define     NULL         0

/**
 * @brief Operation successful.
 */
#define     OK           0U

/**
 * @brief Operation failed.
 */
#define     NOK          1U

/**
 * @brief Null pointer error.
 */
#define     NULL_PTR     2U   


/**
 * @brief Time out error.
 */

#define    TIMEOUT_ERR   3U

/**
 * @brief busy error.
 */
#define   BUSY_ERR      4U

#endif
